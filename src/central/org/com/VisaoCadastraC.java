    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Candidatos a= null;
        int contem = 0;
        String nome = txtNome.getText();
        String cPF = txtCPF.getText();
        String partido = (String) cbxPartido.getSelectedItem();
        int i;
        for(i = 0; VisaoCadastraP.partidos[i]!=null;i++){
            if(VisaoCadastraP.partidos[i].getNome().equals(partido)){
                a = new Candidatos(nome,VisaoCadastraP.partidos[i].getNome(),VisaoCadastraP.partidos[i].getNumero(),cPF);
                
            }
        }
        
        for(i = 0; candidatos[i] != null; i++ ){
            if((candidatos[i].getCPF().equals(cPF))&&(candidatos[i].getPartidoNumero()==a.getPartidoNumero())){
                contem++;
                showMessageDialog(null,"Erro!Candidato j√° cadastrado");
            }
        }
        
        
        if((i<=50)&&(contem < 1)&&(ValidaCPF.isCPF(cPF))){
            candidatos[i] = a;
            showMessageDialog(null,"Cadastro efetuado");
        }
        contem = 0;
        a = null;
            Gson gson = new Gson();
            
            String json = gson.toJson(candidatos);
  
            try {
                //Escreve Json convertido em arquivo chamado "file.json"
                FileWriter writer = new FileWriter("candidatos.json");
                writer.write(json);
                writer.close();
                 

            } catch (IOException e) {
                e.printStackTrace();
            }
  
            
           
            
     
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

