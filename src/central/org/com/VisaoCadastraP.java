    private void bttCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttCadastrarActionPerformed
       int contem = 0;
        PartidoPolitico a = new PartidoPolitico(13,"Lula");
        int i;
       String nome = txtNome.getText();
       int numero = Integer.parseInt(txtNumero.getText());
       a.setNome(nome);
       a.setNumero(numero);
       
        for(i = 0; partidos[i] != null; i++ ){
            if((partidos[i].getNome().equals(a.getNome()))&&(partidos[i].getNumero() == numero)){
                contem++;
                showMessageDialog(null,"Erro!Partido j√° existe");
            }
        }
        if((i <=50)&&(contem<1)){
            
            partidos[i] = a;
            
           Gson gson = new Gson();
           
           String json = gson.toJson(partidos);
           try {
        //Escreve Json convertido em arquivo chamado "file.json"
            FileWriter writer = new FileWriter("partidos.json");
            writer.write(json);
            writer.close();
  
             } catch (IOException e) {
                e.printStackTrace();
             }
  
            
  
    
            
            showMessageDialog(null,"Cadastro efetuado");
            
            
            
        }
        
    }//GEN-LAST:event_bttCadastrarActionPerformed


